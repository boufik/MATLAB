% Από τον νόμο των συνημιτόνων, θα βρω το εξής
% (χορδή)^2 = 2r^2 * (1 - cos(2*pi/N)) και θέλω να έχω: χορδή <= λ/10
% Θα λύσω ως προς το Ν και αυτό θα είναι η min τιμή του

clear all
clc

wavelength = 0.1;
chord = 0.1 * wavelength;
r = 0.3 * wavelength;
N = (2*pi) / acos(1 - chord^2 / (2 * r^2))

